habitats: list[str] = [camel, lion, deer, goose, bat, rabbit]


def main() -> None:
    """ Driver code """
    print(intro_msg)
    usr_input: str = input()

    while usr_input.isdigit():
        num_habitat: int = int(usr_input)
        print(habitats[num_habitat])
        print(intro_msg)
        usr_input = input()

    print(bye_msg)


if __name__ == "__main__":
    main()
